-- INVOICE VIEW
CREATE
	OR REPLACE VIEW ORDER_DB.ORDER__XFRM.V_INVOICE AS

SELECT DISTINCT i.ORDER_ID
	,i.ORDER_DATE
	,i.INVOICE_DATE
	,i.TAX_AMOUNT
	,oh.TOTAL_AMOUNT
	,oh.DISCOUNT
	,i.FINAL_INVOICE_AMT
	,i.CUSTOMER_ID
	,i.CREATE_DATE
	,i.CREATE_USER
FROM ORDER_DB.ORDER__BASE.T_INVOICE i
JOIN ORDER_DB.ORDER__XFRM.V_SALES_ORDER_HEADER oh ON i.ORDER_ID = oh.ORDER_ID