-- SALES_ORDER_DETAIL VIEW 
CREATE
	OR REPLACE VIEW ORDER_DB.ORDER__XFRM.V_SALES_ORDER_DETAIL (
	ORDER_ID
	,ORDER_DATE
	,CUSTOMER_ID
	,ITEM_ID
	,QTY
	,TOTAL_AMOUNT
	,CREATE_DATE
	,CREATE_USER
	,PRICE
	) AS

SELECT ORDER_ID
	,ORDER_DATE
	,CUSTOMER_ID
	,ITEM_ID
	,QTY
	,QTY * PRICE AS TOTAL_AMOUNT
	,CREATE_DATE
	,CREATE_USER
	,PRICE
FROM ORDER_DB.ORDER__BASE.T_SALES_ORDER_DETAIL